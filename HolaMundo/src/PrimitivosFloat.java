public class PrimitivosFloat {
    public static void main(String[] args) {
        float realFloat = 1.5e-10F;
        System.out.println("realFloat = " + realFloat);
        System.out.println("tipo Float corresponede en byte a " + Float.BYTES);
        System.out.println("tipo Float corresponede en bites a " + Float.SIZE);
        System.out.println("Valor maximo de un Float: " + Float.MAX_VALUE);
        System.out.println("Valor minimo de un Float: " + Float.MIN_VALUE);

        double realDouble = 3.4028235E39;
        System.out.println("realDouble = " + realDouble);
        System.out.println("tipo Double corresponede en byte a " + Double.BYTES);
        System.out.println("tipo Double corresponede en bites a " + Double.SIZE);
        System.out.println("Valor maximo de un Double: " + Double.MAX_VALUE);
        System.out.println("Valor minimo de un Double: " + Double.MIN_VALUE);

        float varFlotante = 3.1416F;
        System.out.println("varFlotante = " + varFlotante);
        System.out.println("tipo Double corresponede en byte a " + Double.BYTES);
        System.out.println("tipo Double corresponede en bites a " + Double.SIZE);
        System.out.println("Valor maximo de un Double: " + Double.MAX_VALUE);
        System.out.println("Valor minimo de un Double: " + Double.MIN_VALUE);

    }
}
